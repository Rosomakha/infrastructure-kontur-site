const NPA_CHECKLIST_INTRO_SUFFIX =
  " Пункты чек-листа сформированы с учётом требований нормативных правовых актов, указанных в разделе «Нормативная база»; актуальные редакции и перечни приложений к заявкам уточняются на дату обращения.";

/** Добавляет NPA-блоки в начало docGroups и ссылки на разделы НПБ */
export function mergeNpaChecklists(services: ServiceDetail[]): ServiceDetail[] {
  return services.map((service) => {
    const npaGroups = serviceNpaDocGroups[service.slug];
    const legalSectionIds = serviceLegalSectionIds[service.slug];

    if (!npaGroups?.length && !legalSectionIds?.length) {
      return service;
    }

    const introDocs = service.introDocs.includes("нормативных правовых актов")
      ? service.introDocs
      : `${service.introDocs}${NPA_CHECKLIST_INTRO_SUFFIX}`;

    return {
      ...service,
      introDocs,
      legalSectionIds,
      docGroups: npaGroups?.length
        ? [...npaGroups, ...service.docGroups]
        : service.docGroups
    };
  });
}
