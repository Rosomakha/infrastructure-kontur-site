export function getFederalProgramChecklist(program: FederalSupportProgram): string[] {
  const full = federalProgramChecklists[program.id];
  const base =
    full?.length && full.length > 0
      ? full
      : [...commonApplicant, ...program.briefFields];

  const npaCompliance = program.npas.map(
    (npa) =>
      `Сверка заявочного пакета с требованиями «${npa.title}»${npa.note ? ` (${npa.note})` : ""}: формы, приложения, критерии отбора — действующая редакция.`
  );

  return [...base, ...npaCompliance];
}
